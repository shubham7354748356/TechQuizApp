    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
     if(isValidate())
     {
         JOptionPane.showMessageDialog(null, "PLEASE FILL ALL THE FIELDS FIRST" , "INVALID" , JOptionPane.ERROR_MESSAGE);
         return;
     }
       
      
      userType = isValidate1();
      if(userType==null)
      {
          JOptionPane.showMessageDialog(null, "PLEASE SELECT  THE USER-TYPE" , "USER-NAME" , JOptionPane.ERROR_MESSAGE);
          return;
      }
       
     try
     {
         LoginDetailsPojo pj = new LoginDetailsPojo(userName , password , userType);
     if(LoginDao.validateLogin(pj))
     {
         JOptionPane.showMessageDialog(null, "LOGIN ACCEPTED" , "LOG-IN" , JOptionPane.INFORMATION_MESSAGE);
         UserProfile.setUserNameInProfile(userName);
         UserProfile.setUserTypeInProfile(userType);
         if(userType.equalsIgnoreCase("admin"))
         {
             AdminOptionFrame aof = new AdminOptionFrame();
             aof.setVisible(true);
             this.dispose();
         }
         else
         {
             StudentOptionFrame sof = new StudentOptionFrame();
             sof.setVisible(true);
             this.dispose();
         }
     }  
     else
     {
         JOptionPane.showMessageDialog(null, "INVALID USERNAME/PASSWORD\nLOGIN DENIED" , "LOGIN DENIED" ,JOptionPane.ERROR_MESSAGE);
     }
    
      }
     catch(SQLException sq)
     {
         JOptionPane.showMessageDialog(null, "PROBLEM WITH THE DATABASE" , "ERROR" , JOptionPane.ERROR_MESSAGE);
         sq.printStackTrace();
     }
    }//GEN-LAST:event_btnLoginActionPerformed

