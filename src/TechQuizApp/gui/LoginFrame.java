    private boolean isValidate()
    {
        userName = jtfName.getText();
         char [] pw = jpfPassword.getPassword();
         if(userName.isEmpty()||pw.length==0)
             return true;
         password = String.valueOf(pw);
         return false;
    }
    private String isValidate1()
    {
           if(jrbAdmin.isSelected())
           {
              return jrbAdmin.getText();
           }
          else if(jrbStudent.isSelected())
                   {
                   return jrbStudent.getText();
                   }
           return null;
    }
