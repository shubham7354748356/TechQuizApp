    private void jbtnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNextActionPerformed
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null, "PLEASE FILL ALL THE FIELDS FIRST" , "ERROR" , JOptionPane.ERROR_MESSAGE);
            return;
        }
        QuestionsDetailPojo qdp = new QuestionsDetailPojo();
        qdp.setExamId(edp.getExamId());
        qdp.setQnunmber(ques);
        qdp.setSubject(edp.getExamSubject());
        qdp.setQuestion(question);
        qdp.setOption1(option1);
        qdp.setOption2(option2);
        qdp.setOption3(option3);
        qdp.setOption4(option4);
        qdp.setAnswer(answer);
        alc.removeElement(pos);
        alc.setQuestionAt(qdp, pos);
        pos++;
        if(pos>=alc.getCount())
        {
         JOptionPane.showMessageDialog(null, "YOU HAVE EDITED ALL THE QUESTIONS" , "COMPLETE" , JOptionPane.INFORMATION_MESSAGE);
            pos=0;
        }
        showQuestions();
        ques++;
        if(ques>alc.getCount())
        {
            ques=1;
        }
        jlblQuestionNumber.setText("QUESTION NUMBER :- "+ques);
        
    }//GEN-LAST:event_jbtnNextActionPerformed

    private void jbtnPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPreviousActionPerformed
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null, "PLEASE FILL ALL THE FIELDS FIRST" , "ERROR" , JOptionPane.ERROR_MESSAGE);
            return;
        }
        QuestionsDetailPojo qdp = new QuestionsDetailPojo();
        qdp.setAnswer(answer);
        qdp.setExamId(edp.getExamId());
        qdp.setOption1(option1);
        qdp.setOption2(option2);
        qdp.setOption3(option3);
        qdp.setOption4(option4);
        qdp.setSubject(qdp.getSubject());
        qdp.setQuestion(question);
        qdp.setQnunmber(ques);
        alc.removeElement(pos);
        alc.setQuestionAt(qdp, pos);
        pos--;
        if(pos<0)
        {
            JOptionPane.showMessageDialog(null, "YOU HAVE EDITED ALL THE QUESTIONS" , "COMPLETE" , JOptionPane.INFORMATION_MESSAGE);
            pos = alc.getCount()-1;
        }
        showQuestions();
        ques--;
        if(ques<=0)
        {
            ques = alc.getCount();
        }
        jlblQuestionNumber.setText("QUESTION NUMBER :- "+ques);
        
    }//GEN-LAST:event_jbtnPreviousActionPerformed

