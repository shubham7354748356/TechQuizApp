    private boolean validateInputs()
    {
        question = jtaQuestion.getText().trim();
        option1 = jtfOption1.getText().trim();
        option2 = jtfOption2.getText().trim();
        option3 = jtfOption3.getText().trim();
        option4 = jtfOption4.getText().trim();
        answer = hs.get(jcbCorrectAnswer.getSelectedItem()).trim();
        if(question.isEmpty()||option1.isEmpty()||option2.isEmpty()||option3.isEmpty()||option4.isEmpty()||answer.isEmpty())
        {
            return false;
        }
        return true;
    }
