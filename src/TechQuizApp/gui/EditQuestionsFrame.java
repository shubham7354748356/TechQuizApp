    private String getKeyFromValue(String value)
    {
        Set<String > keys= hs.keySet();
        for(String str :keys)
        {
            if(hs.get(str).equalsIgnoreCase(value))
                return str;
        }
        return null;
    }
private void loadQuestions()
{
   try
   {
       al = QuestionListDao.getAllQuestionsFromExamId(edp.getExamId());
        if(al.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "NO QUESTIONS AVAILABLE" , "ERROR" , JOptionPane.ERROR_MESSAGE);
                return;
            }
        for(QuestionsDetailPojo x: al)
        {
            System.out.println("loop iterated");
            alc.add(x);
        }
   }
   catch(SQLException sq)
         {
             JOptionPane.showMessageDialog(null, "PROBLEM WITH THE DATABASE" , "ERROR" , JOptionPane.ERROR_MESSAGE);
             sq.printStackTrace();
         }
}
