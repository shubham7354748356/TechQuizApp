    private void btnEditPaperActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditPaperActionPerformed
      if(jcbExamId.getSelectedIndex()==-1)
      {
          JOptionPane.showMessageDialog(null, "PLEASE SELECT A SUBJECT FIRST" , "ERROR" , JOptionPane.ERROR_MESSAGE);
          return;
      }
        String examId = jcbExamId.getSelectedItem().toString().trim();
      System.out.println(examId);
      if(examId.isEmpty())
      {
          JOptionPane.showMessageDialog(null, "PLEASE SELECT A SUBJECT FIRST" , "ERROR" , JOptionPane.ERROR_MESSAGE);
          return;
      }
      try
      {
          int questionNumbers = ExamDetailsDao.getQuerstionsNumberFromId(examId);
          ExamDetailsPojo edp = new ExamDetailsPojo(examId , subject , questionNumbers);
          EditQuestionsFrame sqf = new EditQuestionsFrame(edp);
          sqf.setVisible(true);
          this.dispose();
          System.out.println(questionNumbers);
      }
      catch(SQLException sq)
      {
          JOptionPane.showMessageDialog(null, "PROBLEM WITH THE DATABASE" , "ERROR" , JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_btnEditPaperActionPerformed

