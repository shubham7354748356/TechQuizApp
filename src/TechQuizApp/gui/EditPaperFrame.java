    private void jcbExamSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbExamSubjectActionPerformed
       jcbExamId.removeAllItems();
        if(validateSelection())
       {
            JOptionPane.showMessageDialog(null, "PLEASE SELECT A SUBJECT FIRST" , "ERROR" , JOptionPane.ERROR_MESSAGE);
           return;
       }
       try
       {
       ArrayList<String> al = ExamDetailsDao.getAllExamIdFromSubject(subject);
       if(al.isEmpty())
       {
             JOptionPane.showMessageDialog(null, "NO PAPER OF THE SUBJECT FOUND" , "ERROR" , JOptionPane.ERROR_MESSAGE);
           return;
       }
       for(String s:al)
       {
           jcbExamId.addItem(s);
       }
       }
       catch(SQLException s)
       {
            JOptionPane.showMessageDialog(null, "PROBLEM WITH THE DATABASE" , "ERROR" , JOptionPane.ERROR_MESSAGE);
          s.printStackTrace();
       }
    }//GEN-LAST:event_jcbExamSubjectActionPerformed

